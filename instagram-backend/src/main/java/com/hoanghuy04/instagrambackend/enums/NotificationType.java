package com.hoanghuy04.instagrambackend.enums;

/**
 * Enumeration for different types of notifications.
 * Defines the various notification categories in the system.
 * 
 * @author Instagram Backend Team
 * @version 1.0.0
 */
public enum NotificationType {
    /**
     * Notification for post or comment likes
     */
    LIKE,
    
    /**
     * Notification for new comments
     */
    COMMENT,
    
    /**
     * Notification for new followers
     */
    FOLLOW,
    
    /**
     * Notification for new messages
     */
    MESSAGE
}

