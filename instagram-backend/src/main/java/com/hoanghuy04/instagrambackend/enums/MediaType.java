/**
 * Enumeration for media content types.
 * Defines the types of media that can be uploaded to posts.
 * 
 * @author Instagram Backend Team
 * @version 1.0.0
 */
public enum MediaType {
    /**
     * Image media type (JPEG, PNG, GIF, etc.)
     */
    IMAGE,
    
    /**
     * Video media type (MP4, MOV, etc.)
     */
    VIDEO,

    REEL
}

