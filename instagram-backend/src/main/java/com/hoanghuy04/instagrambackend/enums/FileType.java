package com.hoanghuy04.instagrambackend.enums;

/**
 * Enumeration for file upload types.
 * Defines the categories of files that can be uploaded.
 * 
 * @author Instagram Backend Team
 * @version 1.0.0
 */
public enum FileType {
    /**
     * Profile picture/avatar image
     */
    PROFILE_IMAGE,
    
    /**
     * Post media content (images or videos)
     */
    POST_MEDIA,
    
    /**
     * Profile cover/banner image
     */
    COVER_IMAGE
}

